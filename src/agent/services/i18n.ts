const PACKS: Record<LangCode, I18nStrings> = { zh, en, ar, ja, ko, de, fr, es, ru, pt };

let currentLang: LangCode = 'zh';

export function setLanguage(code: LangCode): void {
  currentLang = code;
  const meta = LANGUAGES.find(l => l.code === code);
  if (meta) {
    document.documentElement.dir = meta.dir;
    document.documentElement.lang = code;
  }
}

export function getLanguage(): LangCode {
  return currentLang;
}

export function t(): I18nStrings {
  const pack = PACKS[currentLang] || PACKS.zh;
  // Fallback to en for any missing keys
  if (currentLang !== 'en') {
    return new Proxy(pack, {
      get(target, prop: string) {
        return target[prop] || PACKS.en[prop] || prop;
      }
    }) as I18nStrings;
  }
  return pack;
}

export function detectLanguage(): LangCode {
  const nav = navigator.language?.toLowerCase() || 'zh';
  if (nav.startsWith('zh')) return 'zh';
  if (nav.startsWith('en')) return 'en';
  if (nav.startsWith('ar')) return 'ar';
  if (nav.startsWith('ja')) return 'ja';
  if (nav.startsWith('ko')) return 'ko';
  if (nav.startsWith('de')) return 'de';
  if (nav.startsWith('fr')) return 'fr';
  if (nav.startsWith('es')) return 'es';
  if (nav.startsWith('ru')) return 'ru';
  if (nav.startsWith('pt')) return 'pt';
  return 'en';
}
